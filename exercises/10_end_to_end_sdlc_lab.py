# Exercise 10: End-to-End SDLC Lab
# Feature: Applying GitHub Copilot Across the Full Software Development Lifecycle

"""
This lab guides you through an end-to-end software delivery workflow using Copilot:
requirements, planning, implementation, testing, review, and release readiness.
"""

# TASK 1: Define the change request.
# Prompt Chat:
# "Create a short user story and acceptance criteria for adding budget-based cost alerts."

# TASK 2: Plan the implementation.
# Prompt Chat in Plan/Edits mode:
# "Break this user story into minimal code changes across models/, services/, and main.py."

# TASK 3: Implement with Agent mode.
# Prompt Chat:
# "@workspace Implement the planned changes with type hints and clear error handling."

# TASK 4: Generate tests.
# Prompt Chat:
# "/tests Create focused tests for the new alert behavior and edge cases."

# TASK 5: Validate and review.
# Prompt Chat:
# "Review these changes for correctness, readability, and security risks."
# Then run local tests and manually verify program output.

# TASK 6: Prepare release notes.
# Prompt Chat:
# "Draft a concise PR summary with testing notes and rollout considerations."

# SUCCESS CRITERIA:
# - User story and acceptance criteria are documented
# - Implementation is complete and minimal
# - Tests cover happy path and edge cases
# - Review feedback is incorporated
# - PR summary is ready for team review
